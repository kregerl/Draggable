package com.loucaskreger.draggableui.util;

public class Color4f {

	public float red;
	public float green;
	public float blue;
	public float alpha;

	public Color4f(float red, float green, float blue, float alpha) {
		this.red = red;
		this.green = green;
		this.blue = blue;
		this.alpha = alpha;
	}
}
